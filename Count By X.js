// إنشاء دالة تأخذ رقمين كمدخلات:

// x → الرقم الذي نريد إيجاد مضاعفاته.

// n → عدد المضاعفات التي نريدها.

// الدالة يجب أن ترجع مصفوفة تحتوي على أول n مضاعفات للرقم x
//
// x = 2, n = 4 => [2, 4, 6, 8]
function countBy(x, n) {
  let z = [];
  for (let i = 1; i <= n; i++) {
    z.push(x * i);
  }
  return z;
}
console.log(countBy(2, 4));

